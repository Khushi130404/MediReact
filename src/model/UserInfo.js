class UserInfo {
  constructor(
    userId,
    userName,
    userMail,
    userPass,
    userAge,
    userGender,
    userMobile,
    userAddress
  ) {
    this.userId = userId;
    this.userName = userName;
    this.userMail = userMail;
    this.userPass = userPass;
    this.userAge = userAge;
    this.userGender = userGender;
    this.userMobile = userMobile;
    this.userAddress = userAddress;
  }
}

export default UserInfo;
